package myWork;

public class Ifdemo {
	// Java program to illustrate If statement
		public static void main(String args[])
		{
			String str = "marolix";
			int i = 4;

			// if block
			if (i == 4) {
				i++;
				System.out.println(str);
			}

			// Executed by default
			System.out.println("i = " + i);
		}
	}
